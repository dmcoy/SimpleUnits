namespace SimpleUnits;

/// <summary>
/// Specifies units of length for conversion operations.
/// </summary>
public enum Length
{
    /// <summary>
    /// Length in millimeters.
    /// </summary>
    Millimeters,
    /// <summary>
    /// Length in centimeters.
    /// </summary>
    Centimeters,
    /// <summary>
    /// Length in meters.
    /// </summary>
    Meters,
    /// <summary>
    /// Length in kilometers.
    /// </summary>
    Kilometers,
    /// <summary>
    /// Length in inches.
    /// </summary>
    Inches,
    /// <summary>
    /// Length in feet.
    /// </summary>
    Feet,
    /// <summary>
    /// Length in yards.
    /// </summary>
    Yards,
    /// <summary>
    /// Length in miles.
    /// </summary>
    Miles
}

